    private void updateApp() {
        AppData appData = new AppData(appNameTextField.getText());
        int heartbeatInterval = 0;
        int maxMissedHeartbeats = 0;
        int blockSize = 0;
        int bufSize = 0;
        int bufFlushRate = 0;
        boolean err = false;
        try {
            heartbeatInterval = Integer.parseInt(heartbeatTextField.getText());
        } catch (NumberFormatException e) {
            err = true;
            heartbeatTextField.setBackground(Color.pink);
        }
        try {
            maxMissedHeartbeats = Integer.parseInt(maxMissedHeartbeatsTextField.getText());
        } catch (NumberFormatException e) {
            err = true;
            maxMissedHeartbeatsTextField.setBackground(Color.pink);
        }
        try {
            blockSize = Integer.parseInt(blockSizeTextField.getText());
        } catch (NumberFormatException e) {
            err = true;
            heartbeatTextField.setBackground(Color.pink);
        }

        try {
            bufSize = Integer.parseInt(bufSizeTextField.getText());
        } catch (NumberFormatException e) {
            err = true;
            bufSizeTextField.setBackground(Color.pink);
        }

        try {
            bufFlushRate = Integer.parseInt(bufFlushRateTextField.getText());
        } catch (NumberFormatException e) {
            err = true;
            bufFlushRateTextField.setBackground(Color.pink);
        }

        if (!err) {
            appData.setHeartbeatInterval(heartbeatInterval);
            appData.setMaxMissedHeartbeats(maxMissedHeartbeats);
            appData.setBlockSize(blockSize);
            appData.setBufSize(bufSize);
            appData.setBufFlushRate(bufFlushRate);
            igniter.updateApp(appData);

            heartbeatTextField.setBackground(Color.WHITE);
            maxMissedHeartbeatsTextField.setBackground(Color.WHITE);
            blockSizeTextField.setBackground(Color.WHITE);
        }
    }

    private AppData getAppData() {
        logger.info("Getting data about the app: " + appNameTextField.getText());
        AppData appData = igniter.getAppData(appNameTextField.getText());
        if (!heartbeatTextField.hasFocus() && !maxMissedHeartbeatsTextField.hasFocus() && !blockSizeTextField.hasFocus()) {
            heartbeatTextField.setText(appData.getHeartbeatInterval() + "");
            maxMissedHeartbeatsTextField.setText(appData.getMaxMissedHeartbeats() + "");
            blockSizeTextField.setText(appData.getBlockSize() + "");
        }
        maxIdLabel.setText(appData.getMaxId() + "");
        return appData;
    }

    private void scheduleAppDataUpdate() {
        if (timer == null) {
            timer = new Timer();
        } else {
            timer.cancel();
            timer.purge();
            timer = new Timer();
        }

        numNodesSeries = new XYSeries("# of Nodes");
        numNodesDataset = new XYSeriesCollection();
        numNodesDataset.addSeries(numNodesSeries);
        numNodesSeries.setMaximumItemCount(50);
        timer.schedule(new TimerTask() {
            @Override
            public void run() {
                AppData appData = getAppData();
                int numNodes = appData.getNumNodes();
                numNodesSeries.add(ticks += timerTickLength, numNodes);

                JFreeChart xylineChart = ChartFactory.createXYLineChart(
                        "# of Nodes",
                        "Time",
                        "# of Nodes",
                        numNodesDataset,
                        PlotOrientation.VERTICAL,
                        false, false, false);
                ChartPanel chartPanel = new ChartPanel(null);
                chartPanel.setChart(xylineChart);
                chartPanel.setPreferredSize(new Dimension(400, 300));
                chartContainerPanel.setDoubleBuffered(true);
                chartContainerPanel.setLayout(new FlowLayout());
                chartContainerPanel.remove(0);
                chartContainerPanel.add(chartPanel);
                frame.revalidate();
            }
        }, 100, 1000 * timerTickLength);
    }


